; The table below represents an 8x16 font.  For each 8-bit extended ASCII
; character, the table uses 16 memory locations, each of which contains
; 8 bits (the high 8 bits, for your convenience) marking pixels in the
; line for that character.
; the program prints out big ASCII letter "Harry!" using dots and @ symbols. If the program sees a 0, it prints a ".", but if it finds a "1", it prints an @.
; SYMBOL|ADDRESS
; nCHAR|x300A
; newROW|x3015
; newCOLUMN|x301A
; ZERO|x301F
; ONE|x3024
; FINISHROW|x3028
; FINISH|x302F
; ENTER|x3030
; SPACE|x3031
; ASTERISK|x3032
; FONT_DATA|x3033

		.ORIG x3000; program starts
	
		AND R0,R0, #0; 
		
		AND R1,R1, #0; row counter

		AND R2,R2, #0; column counter

		ADD R1,R1, #8; add counter for row
		
		ADD R1,R1, #8; add counter for row, divided into 2 lines because 2 complement exceeds the offset
	
		AND R3,R3, #0; FONT_DATA address
	
		AND R4,R4, #0; offset to point address
	
		AND R5,R5, #0; ASCII for char
		
		AND R6,R6, #0; store memory address for use
		
	
		LD R6, ASTERISK ; Load data to R6

nCHAR		ADD R6,R6, #1; shift the address by 1 to check another char
		LDR R5,R6, #0; load data register from R6 to R5
		ADD R5,R5, #0; set up CC
		BRz FinishROW ; if R5=0, move to FinishROW
	
		ADD R5,R5,R5; the range for the program execution is 16 lines, thus multiplying by 16 tells where to start
	
		ADD R5,R5,R5
	
		ADD R5,R5,R5
	
	 	ADD R5,R5,R5
	
	
		LEA R3,FONT_DATA ; the address of FONT_DATA is stored into R3
	
		ADD R5,R3,R5; acts as a pointer to locate address in FONT_DATA
		ADD R5,R5,R4; tell where to print
		
		
newROW 		ADD R1,R1, #0; set up CC
		
		BRz FINISH ;if cc = 0, halt 
	
		AND R2,R2, #0; reset column counter
	
		ADD R2,R2, #8; add 8 more counts to column
		
		LDR R3,R5, #0; load register for the address
		
		
newCOLUMN 	ADD R2,R2,#0; set up cc
		
		BRz nCHAR ; if cc = 0, jump to nCHAR command
		
		ADD R3,R3, #0; set up cc
		
		BRn ONE ; if negative, jump to ONE command
		
		BRzp ZERO ; if zero or positive, jump to ZERO command
	
		
ZERO	 	LDI R0, SPACE ; load data immediate from SPACE to R0
	
		OUT; print data stored in R0
		
		ADD R3,R3,R3; add itself in order to left shift and check the next bit in sequence
		
		ADD R2,R2,#-1; -1 from the column counter, finished printing one column
		
		BRzp newCOLUMN ; if zero or positive, jump to column
		
		
ONE	 	LDI R0, ASTERISK ; repeat as the ZERO command
		
		OUT;
		
		ADD R3,R3,R3
	
		ADD R2,R2,#-1
		
		BRzp newCOLUMN
		
	
FinishROW 	LD R0, ENTER ; load the data in ENTER into R0
	
		OUT; print data stored into R0
	
		ADD R1,R1,#-1; -1 row counter to indicate that another row is printed
	
		ADD R4,R4,#1; add 1 to ASCII character
	
		LD R6, ASTERISK ; point to the first string for printing
	
		BRnzp nCHAR ; if R6 is zero or positive, jump to nCHAR command


FINISH 		HALT

ENTER	 	.FILL x0A

SPACE 		.FILL x5000

ASTERISK 	.FILL x5001


